  bool UC8151::is_busy() {
    if(BUSY == PIN_UNUSED) return false;
    return !gpio_get(BUSY);
  }

  void UC8151::busy_wait() {
    while(is_busy()) {
      tight_loop_contents();
    }
  }

  void UC8151::reset() {
    if(RESET == PIN_UNUSED) return;
    gpio_put(RESET, 0); sleep_ms(10);
    gpio_put(RESET, 1); sleep_ms(10);
    busy_wait();
  }

